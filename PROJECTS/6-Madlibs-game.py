# Madlibs game
# word game where you create a story
# by filling in the blank with random words

noun1 = input("Enter a noun (person, place, thing): ")
adjective1 = input("Enter a adjective(description): ")
verb1 = input("Enter a verb ending with 'ing': ")
adjective2 = input("Enter a adjective(description): ")


print(f"Today i watched a movie named {noun1}.")
print(f"I felt {adjective1}, while watching the movie.")
print(f"The main character was {verb1} in the movie.")
print(f"My friend saw a {adjective2} while watching the movie.")
